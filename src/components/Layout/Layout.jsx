// Импортируем React. Это нужно всегда, когда мы пишем компонент.
import React from 'react';
// Импортируем компонент Header, который мы создадим следующим шагом.
import Header from '../Header/Header';
// Импортируем компонент Loader, который создадим позже (по ТЗ).
import Loader from '../Loader/Loader';
// Импортируем CSS-модуль для стилей этого компонента.
import styles from './Layout.module.css';

// Создаем сам компонент Layout.
// Он принимает один параметр (props) - `children`.
// `children` — это ВСЕ, что будет помещено ВНУТРЬ тегов <Layout> ... </Layout> при его использовании.
// В нашем случае — это содержимое страниц (HomePage, NewsPage и т.д.)
const Layout = ({ children }) => {
  return (
    // Это главный div-контейнер всего приложения.
    // `className={styles.layout}` — применяем к нему стили из файла Layout.module.css
    <div className={styles.layout}>
      
      {/* Рендерим компонент Header. Он будет отображаться ВСЕГДА наверху каждой страницы. */}
      <Header />

      {/* Элемент <main>. Это семантический HTML-тег для основного содержимого страницы. */}
      <main className={styles.main}>
        {/* React.Suspense — это встроенный компонент для "ленивой" загрузки других компонентов.
            Если какая-то страница загружается медленно (например, из-за кода), 
            пока она грузится, пользователь увидит Loader.
            `fallback={<Loader />}` — это то, что показывается в ожидании.
            `{children}` — это сама загружаемая страница. */}
        <React.Suspense fallback={<Loader />}>
          {children}
        </React.Suspense>
      </main>

      {/* Здесь в будущем, если понадобится по дизайну, мы добавим подвал (Footer). */}
    </div>
  );
};

// Экспортируем компонент, чтобы его можно было использовать в других файлах (например, в App.jsx).
export default Layout;